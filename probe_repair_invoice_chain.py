"""Probe how repair (warranty) POs get invoiced in SAP.

Goal: figure out whether we should join
    PO -> RSEG -> BKPF         (standard MIRO against PO)
or
    PO -> ESSR -> RSEG -> BKPF (service entry sheet then MIRO)
or something else, when marking a repair ticket as invoiced / closed.

Run:
    python probe_repair_invoice_chain.py

Copy the whole output back to Claude.
"""
from __future__ import annotations

import os
import pyodbc


DSN = os.getenv(
    "SAP_HANA_DSN",
    "DRIVER={HDBODBC};SERVERNODE=10.11.2.25:30241;UID=BAOJIANFENG;PWD=Xja@2025ABC;",
)
MANDT = os.getenv("SAP_CLIENT", "800")

# Known warranty POs from Leo's earlier 1.py -- these are real ZCRM POs
# generated by the C4C repair flow, so whatever path they use is the right
# path for the whole repair dashboard.
SAMPLE_POS = [
    "7000022996",
    "7000022995",
    "7000023009",
    "7000023008",
    "7000022998",
    "7000023027",
]


def section(title: str, sql: str, conn, cap: int = 40) -> None:
    print(f"\n=== {title} ===")
    try:
        cur = conn.execute(sql)
        cols = [c[0] for c in cur.description]
        rows = cur.fetchall()
        if not rows:
            print("  (no rows)")
            return
        header = " | ".join(cols)
        print("  " + header)
        print("  " + "-" * min(len(header), 200))
        for r in rows[:cap]:
            print("  " + " | ".join("" if v is None else str(v).strip() for v in r))
        if len(rows) > cap:
            print(f"  ... and {len(rows) - cap} more")
    except Exception as e:
        print(f"  ERROR: {e}")


def main() -> None:
    po_list = "','".join(SAMPLE_POS)

    with pyodbc.connect(DSN, autocommit=True) as c:

        section(
            "0. EKPO header flags -- doctype, GR flag, SES flag, invoice flag",
            f"""
            SELECT ekpo."EBELN", ekpo."EBELP",
                   ekko."BSART" AS "Doctype",
                   ekpo."WEPOS" AS "GR_Flag",
                   ekpo."REPOS" AS "Inv_Flag",
                   ekpo."PSTYP" AS "ItemCat",
                   ekpo."KNTTP" AS "AcctAssn",
                   ekpo."MENGE" AS "Ordered_Qty",
                   ekpo."WEMNG" AS "GR_Qty"
            FROM "SAPHANADB"."EKPO" ekpo
            INNER JOIN "SAPHANADB"."EKKO" ekko
                ON ekko."MANDT"=ekpo."MANDT" AND ekko."EBELN"=ekpo."EBELN"
            WHERE ekpo."MANDT"='{MANDT}' AND ekpo."EBELN" IN ('{po_list}')
            ORDER BY ekpo."EBELN", ekpo."EBELP"
            """,
            c,
        )

        section(
            "1. EKBE -- PO history. VGABE tells us the transaction type: "
            "1=GR, 2=Invoice, 9=SES. This is the ground truth.",
            f"""
            SELECT "EBELN","EBELP",
                   "VGABE" AS "TxnType",
                   "BELNR" AS "DocNum",
                   "GJAHR" AS "FiscalYr",
                   "BUDAT" AS "PostingDate",
                   "MENGE" AS "Qty",
                   "DMBTR" AS "AmtLocal",
                   "WRBTR" AS "AmtDoc",
                   "WAERS" AS "Currency",
                   "BEWTP" AS "HistoryCat"
            FROM "SAPHANADB"."EKBE"
            WHERE "MANDT"='{MANDT}' AND "EBELN" IN ('{po_list}')
            ORDER BY "EBELN","EBELP","VGABE","BUDAT"
            """,
            c,
        )

        section(
            "2. RSEG -- direct invoice line against the PO (MIRO output)",
            f"""
            SELECT "BELNR" AS "InvDoc",
                   "GJAHR" AS "FiscalYr",
                   "BUZEI" AS "InvItem",
                   "EBELN" AS "PO",
                   "EBELP" AS "POItem",
                   "MENGE" AS "InvQty",
                   "WRBTR" AS "InvAmt",
                   "WAERS" AS "Currency",
                   "SHKZG" AS "DebitCredit"
            FROM "SAPHANADB"."RSEG"
            WHERE "MANDT"='{MANDT}' AND "EBELN" IN ('{po_list}')
            ORDER BY "EBELN","EBELP","BELNR"
            """,
            c,
        )

        section(
            "3. BKPF -- invoice header for whatever RSEG docs exist, "
            "so we can see posting date, status, reversal flag",
            f"""
            SELECT bkpf."BELNR" AS "AcctDoc",
                   bkpf."GJAHR" AS "FiscalYr",
                   bkpf."BLART" AS "DocType",
                   bkpf."BUDAT" AS "PostingDate",
                   bkpf."BLDAT" AS "DocDate",
                   bkpf."XBLNR" AS "RefDoc",
                   bkpf."STBLG" AS "ReversedBy",
                   bkpf."WAERS" AS "Currency"
            FROM "SAPHANADB"."BKPF" bkpf
            WHERE bkpf."MANDT"='{MANDT}'
              AND bkpf."AWKEY" IN (
                SELECT DISTINCT
                       LPAD(rseg."BELNR", 10, '0') || rseg."GJAHR"
                FROM "SAPHANADB"."RSEG" rseg
                WHERE rseg."MANDT"='{MANDT}' AND rseg."EBELN" IN ('{po_list}')
              )
            ORDER BY bkpf."BUDAT" DESC
            """,
            c,
        )

        section(
            "4. ESSR -- service entry sheets against these POs "
            "(only relevant if repair PO uses SES flow)",
            f"""
            SELECT "LBLNI" AS "SES",
                   "EBELN" AS "PO",
                   "EBELP" AS "POItem",
                   "BUDAT" AS "PostingDate",
                   "LWERT" AS "SES_Value",
                   "WAERS" AS "Currency",
                   "LOEKZ" AS "Deleted",
                   "KZABN" AS "AcceptFlag"
            FROM "SAPHANADB"."ESSR"
            WHERE "MANDT"='{MANDT}' AND "EBELN" IN ('{po_list}')
            ORDER BY "EBELN","EBELP","BUDAT"
            """,
            c,
        )

        section(
            "5. Summary: for each PO, do we have GR? Invoice? SES?",
            f"""
            SELECT ekpo."EBELN" AS "PO",
                   COUNT(DISTINCT CASE WHEN ekbe."VGABE"='1' THEN ekbe."BELNR" END) AS "GR_Docs",
                   COUNT(DISTINCT CASE WHEN ekbe."VGABE"='2' THEN ekbe."BELNR" END) AS "Inv_Docs",
                   COUNT(DISTINCT CASE WHEN ekbe."VGABE"='9' THEN ekbe."BELNR" END) AS "SES_Docs",
                   ROUND(SUM(CASE WHEN ekbe."VGABE"='2' THEN ekbe."WRBTR" ELSE 0 END), 2) AS "InvTotal",
                   MAX(CASE WHEN ekbe."VGABE"='2' THEN ekbe."BUDAT" END) AS "LastInvDate"
            FROM "SAPHANADB"."EKPO" ekpo
            LEFT JOIN "SAPHANADB"."EKBE" ekbe
                ON ekbe."MANDT"=ekpo."MANDT"
               AND ekbe."EBELN"=ekpo."EBELN"
               AND ekbe."EBELP"=ekpo."EBELP"
            WHERE ekpo."MANDT"='{MANDT}' AND ekpo."EBELN" IN ('{po_list}')
            GROUP BY ekpo."EBELN"
            ORDER BY ekpo."EBELN"
            """,
            c,
        )


if __name__ == "__main__":
    main()
